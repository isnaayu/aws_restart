# Defining a list
myFruitList=["Apple", "Banana", "Orange"]
print(myFruitList)
print(type(myFruitList))

# Accessing a list by position
print(myFruitList[0])
print(myFruitList[1])
print(myFruitList[2])

# Changing the values in a list
myFruitList[2]="Cherry"
print(myFruitList)

# Introducing the tuple data type
myFinalAnswerTuples = ("Apple", "Banana", "Pinapple")
print(myFinalAnswerTuples)
print(type(myFinalAnswerTuples))

# Accessing a tuple by position
print(myFinalAnswerTuples[0])
print(myFinalAnswerTuples[1])
print(myFinalAnswerTuples[2])

# Introducing the dictionary data type
# Defining a dictionary
myFavoriteFruitDictionary = {
    "Anggi" : "Apple",
    "Isna" : "Orange",
    "Nial" : "Banana"
}
print(myFavoriteFruitDictionary)
print(type(myFavoriteFruitDictionary))

# Accessing a dictionary by name
print(myFavoriteFruitDictionary["Anggi"])
print(myFavoriteFruitDictionary["Isna"])
print(myFavoriteFruitDictionary["Nial"])
